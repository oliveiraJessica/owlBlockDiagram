public class PropValueDTO implements Serializable {

    private PropDTO propertie;
    private String value;

    public PropValueDTO() {
    }

    public PropValueDTO(PropDTO propertie, String value) {
        this.propertie = propertie;
        this.value = value;
    }

    public PropDTO getPropertie() {
        return propertie;
    }

    public void setPropertie(PropDTO propertie) {
        this.propertie = propertie;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

}
